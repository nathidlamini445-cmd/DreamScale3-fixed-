function formatContentForHTML(content: string): string {
  // Escape all content first to prevent XSS
  let escaped = escapeHTML(content);

  // Now apply markdown formatting on the escaped content
  let formatted = escaped
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/\*(.*?)\*/g, '<em>$1</em>')
    .replace(/### (.*)/g, '<h3>$1</h3>')
    .replace(/## (.*)/g, '<h2>$1</h2>')
    .replace(/# (.*)/g, '<h2>$1</h2>')
    .replace(/`(.*?)`/g, '<code>$1</code>');

  const lines = formatted.split('\n');
  const processedLines: string[] = [];
  let inList = false;
  let listType = 'ul';

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i].trim();
    
    if (/^\d+\.\s+/.test(line)) {
      if (!inList || listType !== 'ol') {
        if (inList) processedLines.push(`</${listType}>`);
        processedLines.push('<ol>');
        inList = true;
        listType = 'ol';
      }
      processedLines.push(`<li>${line.replace(/^\d+\.\s+/, '')}</li>`);
    } else if (/^-\s+/.test(line) || /^\*\s+/.test(line)) {
      if (!inList || listType !== 'ul') {
        if (inList) processedLines.push(`</${listType}>`);
        processedLines.push('<ul>');
        inList = true;
        listType = 'ul';
      }
      processedLines.push(`<li>${line.replace(/^[-*]\s+/, '')}</li>`);
    } else {
      if (inList) {
        processedLines.push(`</${listType}>`);
        inList = false;
      }
      if (line && !line.startsWith('&lt;h') && !line.startsWith('&lt;/')) {
        processedLines.push(`<p>${line}</p>`);
      } else if (line) {
        processedLines.push(line);
      }
    }
  }
  
  if (inList) {
    processedLines.push(`</${listType}>`);
  }

  return processedLines.join('\n');
}

function escapeHTML(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

